#include "Enemy.h"

//---------------------------------------------------
//--コンストラクタ・デストラクタ
Enemy::Enemy( ) {
	_position = VGet( 0, 0, 0 );
}


Enemy::~Enemy( ) {
}
//---------------------------------------------------
//---------------------------------------------------


//---------------------------------------------------
//--ゲッター
VECTOR Enemy::GetPosition( ) {
	return _position;
}
